        function countWordsAndChars(textareaId) {
            var text = document.getElementById(textareaId).value;
            var words = text.match(/\S+/g);
            var numWords = words ? words.length : 0;
            var numChars = text.length;

            document.getElementById("count_word").innerHTML = numWords;
            document.getElementById("count_character").innerHTML = numChars;
        }

        var myTextarea = document.getElementById("textbox");
        myTextarea.addEventListener("input", function() {
            countWordsAndChars("myTextarea");
        });


        function toggleCase(textareaId) {
            var textarea = document.getElementById(textareaId);
            var text = textarea.value;
            var toggledText = "";

            for (var i = 0; i < text.length; i++) {
                var char = text.charAt(i);
                if (char === char.toLowerCase()) {
                toggledText += char.toUpperCase();
                } else {
                toggledText += char.toLowerCase();
                }
            }

            textarea.value = toggledText;
        }

        document.getElementById("toggle_case").addEventListener("click", function() {
            toggleCase("textbox");
        });

        function capitalizeText(textarea) {
            var text = textarea.value;
            var lines = text.split("\n");
            for (var i = 0; i < lines.length; i++) {
                var words = lines[i].split(" ");
                for (var j = 0; j < words.length; j++) {
                words[j] = words[j].charAt(0).toUpperCase() + words[j].slice(1);
                }
                lines[i] = words.join(" ");
            }
            textarea.value = lines.join("\n");
        }

        document.getElementById("capitalized_case").addEventListener("click", function() {
            capitalizeText("textbox");
        });

        function sentenceCase(textareaId) {
            var textarea = document.getElementById(textareaId);
            var text = textarea.value.toLowerCase();
            var sentences = text.split('. ');
            for (var i = 0; i < sentences.length; i++) {
                sentences[i] = sentences[i].charAt(0).toUpperCase() + sentences[i].slice(1);
            }
            textarea.value = sentences.join('. ');
        }

        document.getElementById("sentence_case").addEventListener("click", function() {
            sentenceCase("textbox");
        });

        function toLowerCase(textareaId) {
            var textarea = document.getElementById(textareaId);
            var text = textarea.value;
            textarea.value = text.toLowerCase();
        }

        document.getElementById("lower_case").addEventListener("click", function() {
            toLowerCase("textbox");
        });

        function uppercaseTextarea(textareaId) {
            var textarea = document.getElementById(textareaId);
            textarea.value = textarea.value.toUpperCase();
        }

        document.getElementById("upper_case").addEventListener("click", function() {
            uppercaseTextarea("textbox");
        });

        function alternateCase(textareaId) {
            const textarea = document.getElementById(textareaId);
            let newText = '';
            let capitalizeNext = true;

            for (let i = 0; i < textarea.value.length; i++) {
                const char = textarea.value.charAt(i);
                newText += capitalizeNext ? char.toUpperCase() : char.toLowerCase();
                capitalizeNext = !capitalizeNext;
            }

            textarea.value = newText;
        }

        document.getElementById("alternating_case").addEventListener("click", function() {
            alternateCase("textbox");
        });

        function clearTextarea(textareaId) {
            var textarea = document.getElementById(textareaId); 
            textarea.value = ""; 
            document.getElementById("count_word").innerHTML = 0;
            document.getElementById("count_character").innerHTML = 0;
        }

        function copyToClipboard(textboxId) {
            var textbox = document.getElementById(textboxId);
            textbox.select();
            document.execCommand("copy");
        }
